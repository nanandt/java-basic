public class OperasiMtk {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        System.out.println(a - b);
        System.out.println(a + b);
        System.out.println(a / b);
        System.out.println(a * b);
        System.out.println(a % b);

        int c = 10;
        c += 2;
        System.out.println(c);

        c -= 3;
        System.out.println(c);

        c *= 3;
        System.out.println(c);

        int d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);
    }
}
